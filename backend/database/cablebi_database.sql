CREATE OR REPLACE VIEW v_dashboard_general AS
SELECT
    (SELECT COUNT(DISTINCT co.id_cliente)
     FROM contratos co
     WHERE co.estado = 'activo') AS clientes_activos,

    (SELECT COALESCE(SUM(p.monto), 0)
     FROM pagos p
     WHERE p.estado = 'pagado'
       AND DATE_TRUNC('month', p.fecha_pago) = DATE_TRUNC('month', CURRENT_DATE)) AS ingresos_mensuales,

    (SELECT COUNT(*)
     FROM incidencias i
     WHERE DATE_TRUNC('month', i.fecha_reporte) = DATE_TRUNC('month', CURRENT_DATE)) AS reclamos_mes,

    (SELECT ROUND(AVG(e.puntaje)::NUMERIC, 2)
     FROM encuestas_satisfaccion e
     WHERE DATE_TRUNC('month', e.fecha_encuesta) = DATE_TRUNC('month', CURRENT_DATE)) AS satisfaccion_promedio,

    (
        SELECT ROUND(
            (
                COUNT(DISTINCT CASE 
                    WHEN co.estado IN ('cancelado', 'inactivo') 
                    THEN co.id_cliente 
                END)::NUMERIC
                /
                NULLIF(COUNT(DISTINCT co.id_cliente), 0)
            ) * 100, 2
        )
        FROM contratos co
    ) AS churn_rate_general;

CREATE OR REPLACE VIEW v_resumen_sedes AS
SELECT
    s.id_sede,
    s.nombre_sede,
    s.ciudad,
    s.provincia,
    s.region,

    (
        SELECT COUNT(DISTINCT co.id_cliente)
        FROM contratos co
        WHERE co.id_sede = s.id_sede
          AND co.estado = 'activo'
    ) AS clientes_activos,

    (
        SELECT COALESCE(SUM(p.monto), 0)
        FROM pagos p
        INNER JOIN contratos co ON co.id_contrato = p.id_contrato
        WHERE co.id_sede = s.id_sede
          AND p.estado = 'pagado'
    ) AS ingresos_totales,

    (
        SELECT COUNT(*)
        FROM incidencias i
        WHERE i.id_sede = s.id_sede
    ) AS total_reclamos,

    (
        SELECT ROUND(AVG(e.puntaje)::NUMERIC, 2)
        FROM encuestas_satisfaccion e
        INNER JOIN contratos co ON co.id_contrato = e.id_contrato
        WHERE co.id_sede = s.id_sede
    ) AS satisfaccion_promedio,

    (
        SELECT ROUND(
            (
                COUNT(DISTINCT CASE 
                    WHEN co.estado IN ('cancelado', 'inactivo') 
                    THEN co.id_cliente 
                END)::NUMERIC
                /
                NULLIF(COUNT(DISTINCT co.id_cliente), 0)
            ) * 100, 2
        )
        FROM contratos co
        WHERE co.id_sede = s.id_sede
    ) AS churn_rate
FROM sedes s;

CREATE OR REPLACE VIEW v_kpis_mensuales AS
SELECT
    EXTRACT(YEAR FROM p.fecha_emision)::INT AS anio,
    EXTRACT(MONTH FROM p.fecha_emision)::INT AS mes,
    COUNT(DISTINCT co.id_cliente) AS clientes_con_pago,
    COALESCE(SUM(CASE WHEN p.estado = 'pagado' THEN p.monto ELSE 0 END), 0) AS ingresos,
    COUNT(CASE WHEN p.estado IN ('pendiente', 'vencido') THEN 1 END) AS pagos_pendientes,
    ROUND(
        COUNT(CASE WHEN p.estado IN ('pendiente', 'vencido') THEN 1 END)::NUMERIC
        / NULLIF(COUNT(p.id_pago), 0) * 100, 2
    ) AS tasa_morosidad
FROM pagos p
INNER JOIN contratos co ON co.id_contrato = p.id_contrato
GROUP BY
    EXTRACT(YEAR FROM p.fecha_emision),
    EXTRACT(MONTH FROM p.fecha_emision);

CREATE OR REPLACE VIEW v_tiempo_resolucion_incidencias AS
SELECT
    i.id_sede,
    s.nombre_sede,
    COUNT(*) AS total_incidencias,
    COUNT(CASE WHEN i.estado IN ('resuelta', 'cerrada') THEN 1 END) AS incidencias_resueltas,
    ROUND(
        AVG(
            EXTRACT(EPOCH FROM (i.fecha_resolucion - i.fecha_reporte)) / 3600
        )::NUMERIC, 2
    ) AS horas_promedio_resolucion
FROM incidencias i
INNER JOIN sedes s ON s.id_sede = i.id_sede
WHERE i.fecha_resolucion IS NOT NULL
GROUP BY i.id_sede, s.nombre_sede;

CREATE OR REPLACE VIEW v_clientes_ml_features AS
SELECT
    c.id_cliente,
    c.dni,
    c.nombres,
    c.apellidos,
    c.estado AS estado_cliente,

    COALESCE(MAX(pl.precio_mensual), 0) AS precio_max_plan,
    COALESCE(MAX(pl.velocidad_mbps), 0) AS velocidad_max_mbps,
    COUNT(DISTINCT co.id_contrato) AS num_contratos,
    COALESCE(SUM(CASE WHEN p.estado = 'pagado' THEN p.monto ELSE 0 END), 0) AS monto_total_pagado,
    COUNT(DISTINCT p.id_pago) AS num_pagos,
    COUNT(DISTINCT CASE WHEN p.estado IN ('pendiente', 'vencido') THEN p.id_pago END) AS pagos_pendientes,
    COUNT(DISTINCT i.id_incidencia) AS num_incidencias,
    COALESCE(ROUND(AVG(e.puntaje)::NUMERIC, 2), 0) AS satisfaccion_promedio,
    COALESCE(c.id_sede, 0) AS sede_codificada,

    CASE
        WHEN EXISTS (
            SELECT 1
            FROM contratos co2
            WHERE co2.id_cliente = c.id_cliente
              AND co2.estado IN ('cancelado', 'inactivo')
        ) THEN 1
        ELSE 0
    END AS churn

FROM clientes c
LEFT JOIN contratos co ON co.id_cliente = c.id_cliente
LEFT JOIN planes pl ON pl.id_plan = co.id_plan
LEFT JOIN pagos p ON p.id_contrato = co.id_contrato
LEFT JOIN incidencias i ON i.id_contrato = co.id_contrato
LEFT JOIN encuestas_satisfaccion e ON e.id_contrato = co.id_contrato
GROUP BY
    c.id_cliente,
    c.dni,
    c.nombres,
    c.apellidos,
    c.estado,
    c.id_sede;

