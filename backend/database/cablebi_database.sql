DROP SCHEMA IF EXISTS cablebi CASCADE;
CREATE SCHEMA cablebi;
SET search_path TO cablebi;

-- =========================================================
-- 1. TABLAS DE SEGURIDAD
-- =========================================================

CREATE TABLE roles (
    id_rol INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_rol VARCHAR(50) NOT NULL UNIQUE,
    descripcion TEXT
);

CREATE TABLE usuarios (
    id_usuario INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_rol INT NOT NULL,
    nombres VARCHAR(100) NOT NULL,
    apellidos VARCHAR(100) NOT NULL,
    usuario VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(100) NOT NULL UNIQUE,
    password_hash TEXT NOT NULL,
    estado VARCHAR(20) NOT NULL DEFAULT 'activo',
    fecha_creacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_usuario_rol
        FOREIGN KEY (id_rol) REFERENCES roles(id_rol),

    CONSTRAINT chk_usuario_estado
        CHECK (estado IN ('activo', 'inactivo'))
);

-- =========================================================
-- 2. TABLAS MAESTRAS
-- =========================================================

CREATE TABLE sedes (
    id_sede INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_sede VARCHAR(100) NOT NULL,
    ciudad VARCHAR(100) NOT NULL,
    provincia VARCHAR(100),
    region VARCHAR(100) NOT NULL,
    direccion VARCHAR(150) NOT NULL,
    telefono VARCHAR(20),
    es_expansion BOOLEAN NOT NULL DEFAULT FALSE,
    estado VARCHAR(20) NOT NULL DEFAULT 'activo',

    CONSTRAINT chk_sede_estado
        CHECK (estado IN ('activo', 'inactivo'))
);

CREATE TABLE clientes (
    id_cliente INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_sede INT,
    dni VARCHAR(15) NOT NULL UNIQUE,
    nombres VARCHAR(80) NOT NULL,
    apellidos VARCHAR(80) NOT NULL,
    telefono VARCHAR(20),
    email VARCHAR(100) UNIQUE,
    direccion VARCHAR(150),
    tipo_cliente VARCHAR(20) NOT NULL DEFAULT 'natural',
    fecha_registro DATE NOT NULL DEFAULT CURRENT_DATE,
    estado VARCHAR(20) NOT NULL DEFAULT 'activo',

    CONSTRAINT fk_cliente_sede
        FOREIGN KEY (id_sede) REFERENCES sedes(id_sede),

    CONSTRAINT chk_cliente_tipo
        CHECK (tipo_cliente IN ('natural', 'juridico')),

    CONSTRAINT chk_cliente_estado
        CHECK (estado IN ('activo', 'inactivo'))
);

CREATE TABLE planes (
    id_plan INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_plan VARCHAR(100) NOT NULL,
    tipo_servicio VARCHAR(30) NOT NULL,
    categoria_comercial VARCHAR(30) NOT NULL,
    velocidad_mbps INT NOT NULL,
    precio_mensual NUMERIC(10,2) NOT NULL,
    estado VARCHAR(20) NOT NULL DEFAULT 'activo',

    CONSTRAINT chk_plan_servicio
        CHECK (tipo_servicio IN ('cable', 'internet', 'combo')),

    CONSTRAINT chk_plan_categoria
        CHECK (categoria_comercial IN (
            'basico',
            'estandar',
            'plus',
            'premium',
            'empresarial',
            'corporativo'
        )),

    CONSTRAINT chk_plan_estado
        CHECK (estado IN ('activo', 'inactivo')),

    CONSTRAINT chk_plan_velocidad
        CHECK (velocidad_mbps > 0),

    CONSTRAINT chk_plan_precio
        CHECK (precio_mensual >= 0)
);

CREATE TABLE segmentos_kmeans (
    id_segmento INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cluster_kmeans INT NOT NULL UNIQUE,
    nombre_segmento VARCHAR(80) NOT NULL,
    descripcion TEXT,
    estrategia_marketing TEXT,

    CONSTRAINT chk_cluster_kmeans
        CHECK (cluster_kmeans BETWEEN 1 AND 4)
);

-- =========================================================
-- 3. TABLAS TRANSACCIONALES
-- =========================================================

CREATE TABLE contratos (
    id_contrato INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INT NOT NULL,
    id_sede INT NOT NULL,
    id_plan INT NOT NULL,
    fecha_inicio DATE NOT NULL,
    fecha_fin DATE,
    estado VARCHAR(20) NOT NULL DEFAULT 'activo',
    observacion TEXT,

    CONSTRAINT fk_contrato_cliente
        FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),

    CONSTRAINT fk_contrato_sede
        FOREIGN KEY (id_sede) REFERENCES sedes(id_sede),

    CONSTRAINT fk_contrato_plan
        FOREIGN KEY (id_plan) REFERENCES planes(id_plan),

    CONSTRAINT chk_contrato_estado
        CHECK (estado IN ('activo', 'suspendido', 'cancelado', 'inactivo')),

    CONSTRAINT chk_contrato_fechas
        CHECK (fecha_fin IS NULL OR fecha_fin >= fecha_inicio)
);

CREATE TABLE pagos (
    id_pago INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_contrato INT NOT NULL,
    fecha_emision DATE NOT NULL DEFAULT CURRENT_DATE,
    fecha_pago DATE,
    monto NUMERIC(10,2) NOT NULL,
    metodo_pago VARCHAR(30),
    referencia VARCHAR(100),
    estado VARCHAR(20) NOT NULL DEFAULT 'pendiente',

    CONSTRAINT fk_pago_contrato
        FOREIGN KEY (id_contrato) REFERENCES contratos(id_contrato),

    CONSTRAINT chk_pago_monto
        CHECK (monto >= 0),

    CONSTRAINT chk_pago_estado
        CHECK (estado IN ('pagado', 'pendiente', 'vencido', 'anulado')),

    CONSTRAINT chk_pago_metodo
        CHECK (
            metodo_pago IS NULL OR metodo_pago IN (
                'efectivo',
                'transferencia',
                'yape',
                'plin',
                'tarjeta',
                'deposito'
            )
        )
);

CREATE TABLE incidencias (
    id_incidencia INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INT NOT NULL,
    id_contrato INT NOT NULL,
    id_sede INT NOT NULL,
    fecha_reporte TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    tipo_incidencia VARCHAR(50) NOT NULL,
    descripcion TEXT NOT NULL,
    prioridad VARCHAR(20) NOT NULL DEFAULT 'media',
    estado VARCHAR(20) NOT NULL DEFAULT 'registrada',
    fecha_resolucion TIMESTAMP,

    CONSTRAINT fk_incidencia_cliente
        FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),

    CONSTRAINT fk_incidencia_contrato
        FOREIGN KEY (id_contrato) REFERENCES contratos(id_contrato),

    CONSTRAINT fk_incidencia_sede
        FOREIGN KEY (id_sede) REFERENCES sedes(id_sede),

    CONSTRAINT chk_incidencia_prioridad
        CHECK (prioridad IN ('baja', 'media', 'alta', 'critica')),

    CONSTRAINT chk_incidencia_estado
        CHECK (estado IN ('registrada', 'en_proceso', 'resuelta', 'cerrada')),

    CONSTRAINT chk_incidencia_fechas
        CHECK (fecha_resolucion IS NULL OR fecha_resolucion >= fecha_reporte)
);

CREATE TABLE encuestas_satisfaccion (
    id_encuesta INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INT NOT NULL,
    id_contrato INT NOT NULL,
    fecha_encuesta DATE NOT NULL DEFAULT CURRENT_DATE,
    puntaje INT NOT NULL,
    comentario TEXT,

    CONSTRAINT fk_encuesta_cliente
        FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),

    CONSTRAINT fk_encuesta_contrato
        FOREIGN KEY (id_contrato) REFERENCES contratos(id_contrato),

    CONSTRAINT chk_encuesta_puntaje
        CHECK (puntaje BETWEEN 1 AND 5)
);

-- =========================================================
-- 4. TABLAS PARA MACHINE LEARNING
-- =========================================================

CREATE TABLE cliente_segmentacion (
    id_cliente_segmentacion INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INT NOT NULL,
    id_segmento INT NOT NULL,
    fecha_segmentacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    algoritmo VARCHAR(50) NOT NULL DEFAULT 'K-Means',
    observacion TEXT,

    CONSTRAINT fk_segmentacion_cliente
        FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),

    CONSTRAINT fk_segmentacion_segmento
        FOREIGN KEY (id_segmento) REFERENCES segmentos_kmeans(id_segmento)
);

CREATE TABLE predicciones_churn (
    id_prediccion INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente INT NOT NULL,
    fecha_prediccion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    probabilidad_churn NUMERIC(5,2) NOT NULL,
    nivel_riesgo VARCHAR(20) NOT NULL,
    algoritmo VARCHAR(50) NOT NULL DEFAULT 'Random Forest',
    recomendacion TEXT,

    CONSTRAINT fk_churn_cliente
        FOREIGN KEY (id_cliente) REFERENCES clientes(id_cliente),

    CONSTRAINT chk_churn_probabilidad
        CHECK (probabilidad_churn BETWEEN 0 AND 100),

    CONSTRAINT chk_churn_riesgo
        CHECK (nivel_riesgo IN ('bajo', 'medio', 'alto'))
);

