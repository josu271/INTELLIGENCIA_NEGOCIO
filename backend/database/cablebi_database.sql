CREATE OR REPLACE VIEW dim_cliente AS
SELECT
    c.id_cliente,
    CONCAT(c.nombres, ' ', c.apellidos) AS nombre_cliente,
    c.tipo_cliente,
    c.id_sede,
    c.estado AS estado_cliente,
    sk.cluster_kmeans AS segmento_kmeans
FROM clientes c
LEFT JOIN cliente_segmentacion cs ON cs.id_cliente = c.id_cliente
LEFT JOIN segmentos_kmeans sk ON sk.id_segmento = cs.id_segmento;

CREATE OR REPLACE VIEW dim_sede AS
SELECT
    id_sede,
    nombre_sede,
    region,
    provincia,
    ciudad,
    es_expansion
FROM sedes;

CREATE OR REPLACE VIEW dim_plan AS
SELECT
    id_plan,
    nombre_plan,
    velocidad_mbps,
    precio_mensual,
    categoria_comercial,
    tipo_servicio
FROM planes;

CREATE OR REPLACE VIEW fact_contrato AS
SELECT
    co.id_contrato,
    co.id_cliente,
    co.id_plan,
    co.id_sede,
    (EXTRACT(YEAR FROM co.fecha_inicio)::INT * 100 + EXTRACT(MONTH FROM co.fecha_inicio)::INT) AS id_tiempo,
    co.estado AS estado_contrato,
    co.fecha_fin
FROM contratos co;

CREATE OR REPLACE VIEW fact_pago AS
SELECT
    p.id_pago,
    p.id_contrato,
    (EXTRACT(YEAR FROM p.fecha_emision)::INT * 100 + EXTRACT(MONTH FROM p.fecha_emision)::INT) AS id_tiempo,
    p.monto AS monto_pago,
    p.estado AS estado_pago,
    p.fecha_emision,
    p.fecha_pago
FROM pagos p;

